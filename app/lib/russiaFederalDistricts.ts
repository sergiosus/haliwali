const SUBJECT_TO_DISTRICT = (() => {
  const m = new Map<string, FederalDistrictLabel>();
  for (const d of FEDERAL_DISTRICTS) {
    for (const s of SUBJECTS_BY_DISTRICT[d]) m.set(s, d);
  }
  return m;
})();

export function subjectsForFederalDistrict(label: string): string[] {
  const t = (label ?? "").trim();
  const d = (FEDERAL_DISTRICTS as readonly string[]).find((x) => x === t) as FederalDistrictLabel | undefined;
  if (!d) return [];
  return [...SUBJECTS_BY_DISTRICT[d]];
}

export function federalDistrictForSubject(subject: string): FederalDistrictLabel | null {
  const s = (subject ?? "").trim();
  return (SUBJECT_TO_DISTRICT.get(s) ?? null) as FederalDistrictLabel | null;
}

