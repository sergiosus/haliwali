function collapseKey(s: string): string {
  return s
    .trim()
    .toLowerCase()
    .replace(/\s+/g, " ")
    .replace(/ё/g, "е");
}

/** Single display label per logical region (for dropdown + `listingMatchesSearchScope`). */
export function canonicalRussiaRegionLabel(raw: string): string {
  const t = raw.trim();
  if (!t) return t;
  const key = collapseKey(t);
  return REGION_TO_CANONICAL[key] ?? t;
}

/** Sorted unique canonical regions derived from static city data (no duplicates). */
export function uniqueCanonicalRussiaRegions(): string[] {
  const seen = new Set<string>();
  for (const c of getGlobalRussiaCitiesForSearch()) {
    const r = (c.region ?? "").trim();
    if (!r) continue;
    seen.add(canonicalRussiaRegionLabel(r));
  }
  return [...seen].sort((a, b) => a.localeCompare(b, "ru"));
}
