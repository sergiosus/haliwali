#!/usr/bin/env node
/**
 * One-off migration: `.data/ads.json` (legacy minimal index) → PostgreSQL `listings` table.
 *
 * Usage:
 *   DATABASE_URL=postgres://... node scripts/import-ads-json-to-pg.mjs
 *
 * Rows missing description/images get placeholders so the full listing shape is valid.
 */
import fs from "node:fs";
import path from "node:path";
import os from "node:os";
import { spawnSync } from "node:child_process";

const DATABASE_URL = process.env.DATABASE_URL?.trim();
if (!DATABASE_URL) {
  console.error("DATABASE_URL is required");
  process.exit(1);
}

const adsPath = path.join(process.cwd(), ".data", "ads.json");
if (!fs.existsSync(adsPath)) {
  console.error("Missing", adsPath);
  process.exit(1);
}

const map = JSON.parse(fs.readFileSync(adsPath, "utf8"));
if (!map || typeof map !== "object") {
  console.error("Invalid ads.json shape");
  process.exit(1);
}

function inferType(id) {
  const s = String(id).toLowerCase();
  if (s.startsWith("task-")) return "task";
  if (s.startsWith("service-")) return "service";
  return "product_sell";
}

const now = Date.now();

function sqlString(v) {
  const s = String(v ?? "");
  return `'${s.replace(/'/g, "''")}'`;
}

function sqlNullableNumber(v) {
  if (typeof v === "number" && Number.isFinite(v)) return String(Math.trunc(v));
  if (typeof v === "string" && v.trim()) {
    const n = Number(v);
    if (Number.isFinite(n)) return String(Math.trunc(n));
  }
  return "NULL";
}

const statements = [];
statements.push("-- Generated by scripts/import-ads-json-to-pg.mjs");
statements.push("BEGIN;");

for (const [key, row] of Object.entries(map)) {
  if (!row || typeof row !== "object") continue;
  const id = typeof row.id === "string" && row.id.trim() ? row.id.trim() : key.trim();
  if (!id) continue;

  const title = typeof row.title === "string" ? row.title.trim() : "";
  const city = typeof row.city === "string" ? row.city.trim() : "";
  const categoryName = typeof row.category === "string" ? row.category.trim() : "";
  const categorySlug = typeof row.categorySlug === "string" ? row.categorySlug.trim() : "";
  const status = typeof row.status === "string" ? row.status.trim() : "auto";
  const ownerId = typeof row.ownerId === "string" ? row.ownerId.trim() : "";
  const dealStatus = typeof row.dealStatus === "string" ? row.dealStatus.trim() : "active";
  const rawPrice = typeof row.price === "number" && Number.isFinite(row.price) ? Math.trunc(row.price) : null;
  const images = Array.isArray(row.images) ? row.images.filter((x) => typeof x === "string") : [];

  if (!title || !city || !categoryName || !categorySlug || !status) continue;

  const type = inferType(id);
  const editToken = `${id}-import`;
  const priceCol = type === "product_sell" || type === "product_buy" ? rawPrice : null;

  const photosJson = JSON.stringify(images);
  const desc = "(Импорт из .data/ads.json — обновите описание вручную при необходимости.)";
  const specializationCol = type === "service" ? sqlString("") : "NULL";

  statements.push(
    [
      "INSERT INTO listings (",
      "  id, edit_token, owner_id, type, status, moderation_reason, deal_status,",
      "  title, description, category_name, category_slug, city, address, latitude, longitude,",
      "  address_public, specialization, price, phone, photos, created_at, updated_at",
      ") VALUES (",
      `  ${sqlString(id)},`,
      `  ${sqlString(editToken)},`,
      `  ${sqlString(ownerId || "unknown")},`,
      `  ${sqlString(type)},`,
      `  ${sqlString(status)},`,
      "  '',",
      `  ${sqlString(dealStatus)},`,
      `  ${sqlString(title)},`,
      `  ${sqlString(desc)},`,
      `  ${sqlString(categoryName)},`,
      `  ${sqlString(categorySlug)},`,
      `  ${sqlString(city)},`,
      "  NULL, NULL, NULL,",
      "  FALSE,",
      `  ${specializationCol},`,
      `  ${sqlNullableNumber(priceCol)},`,
      "  '',",
      `  ${sqlString(photosJson)}::jsonb,`,
      `  ${String(now)},`,
      `  ${String(now)}`,
      ")",
      "ON CONFLICT (id) DO UPDATE SET",
      "  title = EXCLUDED.title,",
      "  city = EXCLUDED.city,",
      "  category_name = EXCLUDED.category_name,",
      "  category_slug = EXCLUDED.category_slug,",
      "  status = EXCLUDED.status,",
      "  deal_status = EXCLUDED.deal_status,",
      "  owner_id = EXCLUDED.owner_id,",
      "  photos = EXCLUDED.photos,",
      "  price = EXCLUDED.price,",
      "  updated_at = EXCLUDED.updated_at;",
    ].join("\n"),
  );
}

statements.push("COMMIT;");

const outPath = path.join(os.tmpdir(), `haliwali-import-ads-${now}.sql`);
fs.writeFileSync(outPath, `${statements.join("\n")}\n`, "utf8");

const r = spawnSync("psql", [DATABASE_URL, "-v", "ON_ERROR_STOP=1", "-f", outPath], {
  stdio: "inherit",
});

if (r.status !== 0) {
  console.error("psql failed. SQL file left at:", outPath);
  process.exit(r.status ?? 1);
}

console.log("Import complete. SQL file:", outPath);
