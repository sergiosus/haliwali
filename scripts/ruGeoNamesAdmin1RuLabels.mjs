export function regionLabelFromGeoNamesAdmin1Code(raw) {
  const s = `${raw ?? ""}`.trim();
  if (!s || s === "JA") return "";
  const key = /^\d+$/.test(s) ? s.padStart(2, "0") : s;
  return RU_GEO_NAMES_ADMIN1_RU_LABEL[key] ?? "";
}
